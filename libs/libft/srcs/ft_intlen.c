#include <libft.h>

int		ft_intlen(long n)
{
	int len;

	len = 1;
	n < 0 ? ++len : 0;
	n < 0 ? n = -n : 0;
	while (n > 9)
	{
		n /= 10;
		++len;
	}
	return (len);
}
